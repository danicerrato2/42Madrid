#include <stdio.h>
#include <unistd.h>

void 	ft_putnbr(int nb)
{
	char	digit;

	if (nb < 0)
	{
		write (1, "-", 1);
		nb *= (-1);
	}
	while (nb > 9)
	{
		digit = '0' + nb % 10;
		write (1, &digit, 1),
		nb /= 10;
	}
}
